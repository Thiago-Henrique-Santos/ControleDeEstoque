    private void jButton18ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton18ActionPerformed
        //Pegando id da compra
        int codigoCompra = Integer.parseInt(idCompra_atualizar.getText());
        //Objeto para receber a compra que virá do método "buscarCompra"
        Compra compra = new Compra();
        //Consultando compra
        compra = App.buscarCompra(codigoCompra);
        
        //Mostrando os dados ou alertando que a compra não foi encontrada
        if (compra == null) {
            JOptionPane.showMessageDialog(this, "A compra não foi encontrada!");
        }else{
            //Mostrando dados
            //Nome do cliente responsável
            lbl_clienteResponsavel_atualizarCompra.setText(compra.getClienteResponsavel().getNomeCliente() + "");
            
            //Verificador - existência de itens na compra
            int AtualizarCompra_Buscar_Verificador1 = compra.getProdutosComprados().size();

            //Atualizando textArea de produtos
            String textoDaAreaDeTexto = "";
            //Se houver produtos, na compra, mostrará os produtos
            //senão deixará a área de texto em branco
            if(AtualizarCompra_Buscar_Verificador1 > 0){
                for (int i = 0; i < compra.getProdutosComprados().size(); i++) {
                    textArea_showProdutosDaCompra_atualizar.setText(textoDaAreaDeTexto + compra.getProdutosComprados().get(i).getNomeProduto() + " - R$" + compra.getProdutosComprados().get(i).getPreco() + "\n");
                    textoDaAreaDeTexto = textArea_showProdutosDaCompra_atualizar.getText();
                }
            }else{
                textArea_showProdutosDaCompra_atualizar.setText("");
            }
        }
    }//GEN-LAST:event_jButton18ActionPerformed

