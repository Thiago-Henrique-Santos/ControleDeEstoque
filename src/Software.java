    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Pegando os valores
        String nomeCliente = nomeCliente_cadastrar.getText();
        String emailCliente = emailCliente_cadastrar.getText();
        String telefoneCliente = telefoneCliente_cadastrar.getText();
        String bairroCliente = bairroCliente_cadastrar.getText();
        String ruaCliente = ruaCliente_cadastrar.getText();
        String numeroCasaCliente = numeroCasaCliente_cadastrar.getText();
        //convertendo número da casa que está em String, para inteiro
        int numeroCasa = Integer.parseInt(numeroCasaCliente);
        
        //Cadastrando o cliente
        int id; //Nome da variável está id, pois após inserir os dados, para cadastrar, ela recebe o ID do cliente
        id = App.cadastrarCliente(nomeCliente, emailCliente, telefoneCliente, bairroCliente, ruaCliente, numeroCasa);
        
        //Alertando que o cliente foi cadastrado e limpando caixas de texto
        JOptionPane.showMessageDialog(this, "O cliente foi cadastrado com sucesso!\nO código deste cliente é: " + id + ".");
        nomeCliente_cadastrar.setText("");
        emailCliente_cadastrar.setText("");
        telefoneCliente_cadastrar.setText("");
        bairroCliente_cadastrar.setText("");
        ruaCliente_cadastrar.setText("");
        numeroCasaCliente_cadastrar.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

